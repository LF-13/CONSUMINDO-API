public class Conteudo {

    private final String tilulo;
    private final String urlImagem;

    public Conteudo(String tilulo, String urlImagem) {
        this.tilulo = tilulo;
        this.urlImagem = urlImagem;
    }

    public String getTilulo() {
        return tilulo;
    }

    public String getUrlImagem() {
        return urlImagem;

    }
}
